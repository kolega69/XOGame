package players;

public class Player {

	String name;
	int coordC;
	int coordR;

	public Player() {
	}

	public Player(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public int getCoordC() {
		return coordC;
	}

	public int getCoordR() {
		return coordR;
	}
}
